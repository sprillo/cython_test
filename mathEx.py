def stat2Num(x, y):
    """ Sum and mean value of two numbers

    Parameters
    ----------
    x : float
        first number to be added

    y : float
        second number two be added

    Returns
    -------
    x+y: float
        sum of two numbers
    (x+y)/2 : float
        mean of two numbers
    """
